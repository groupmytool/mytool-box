public class KvComboBoxEntity {

  private String name;
  private String value;

  public KvComboBoxEntity(String name, String value) {
    this.name = name;
    this.value = value;
  }

  public String getName() {
    return name;
  }

  public String getValue() {
    return value;
  }
}
